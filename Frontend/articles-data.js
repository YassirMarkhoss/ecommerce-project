/* Helper: get article by id */
function getArticleById(id) {
  return ARTICLES.find(a => a.id === id);
}

/* Helper: get related articles (same category, excluding current) */
function getRelatedArticles(currentId, count = 3) {
  const current = getArticleById(currentId);
  if (!current) return ARTICLES.slice(0, count);

  const sameCategory = ARTICLES.filter(a => a.id !== currentId && a.category === current.category);
  const others = ARTICLES.filter(a => a.id !== currentId && a.category !== current.category);

  return [...sameCategory, ...others].slice(0, count);
}
